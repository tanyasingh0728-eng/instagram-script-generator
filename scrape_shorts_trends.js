'use strict';

/**
 * scrape_shorts_trends.js
 *
 * Searches YouTube Shorts for competitor-intel queries, extracts transcripts/metadata via
 * Apify, and compiles a styled HTML -> PDF report via WeasyPrint.
 *
 * Actor note: "apify/youtube-scraper" does not exist on Apify. The verified, actively
 * maintained equivalent is "streamers/youtube-scraper" (88k+ users), which supports
 * keyword search + a Shorts-specific result cap + native view-count sorting + subtitle
 * download - confirmed against its real input schema before writing this script.
 *
 * Required environment variables (.env in this folder):
 *   APIFY_API_TOKEN - Apify API token
 *
 * Required local tooling (not npm packages):
 *   WeasyPrint (Python) + its system deps (Pango, GLib via Homebrew). On this machine the
 *   WeasyPrint console script is at WEASYPRINT_BIN below; macOS's System Integrity Protection
 *   strips DYLD_LIBRARY_PATH for signed system binaries like /usr/bin/python3, so invoking
 *   `python3 -m weasyprint` silently fails to find libgobject - the installed console script
 *   is not SIP-hardened the same way and works once DYLD_LIBRARY_PATH points at the Homebrew
 *   lib directory.
 */

require('dotenv').config();

const fs = require('fs');
const path = require('path');
const { execFileSync } = require('child_process');
const { ApifyClient } = require('apify-client');
const { brandConfig } = require('./config');

const ACTOR_ID = 'streamers/youtube-scraper';
const SEARCH_QUERIES = brandConfig.searchKeywords;
const MAX_SHORTS_PER_SEARCH = 20;
const TOP_OVERALL_COUNT = 20;

// Fonts are shared rendering infrastructure (not brand-specific), so they stay at the project
// root regardless of which brand's output folder the HTML/PDF land in.
const FONTS_DIR = path.join(__dirname, 'fonts');

const BRAND_DIR = path.join(__dirname, 'brands', brandConfig.folder);
fs.mkdirSync(BRAND_DIR, { recursive: true });
const HTML_OUTPUT_PATH = path.join(BRAND_DIR, 'Competitor_Viral_Shorts_Transcripts.html');
const PDF_OUTPUT_PATH = path.join(BRAND_DIR, 'Competitor_Viral_Shorts_Transcripts.pdf');

const WEASYPRINT_BIN = '/Users/tanyasingh/Library/Python/3.9/bin/weasyprint';
const HOMEBREW_LIB_DIR = '/opt/homebrew/lib';

function requireEnv(name) {
  const value = process.env[name];
  if (!value) {
    throw new Error(`Missing required environment variable: ${name}. Set it in a .env file in this folder.`);
  }
  return value;
}

function escapeHtml(value) {
  return (value ?? '')
    .toString()
    .replace(/&/g, '&amp;')
    .replace(/</g, '&lt;')
    .replace(/>/g, '&gt;')
    .replace(/"/g, '&quot;')
    .replace(/'/g, '&#39;');
}

function formatNumber(n) {
  return typeof n === 'number' ? n.toLocaleString('en-US') : 'N/A';
}

// ---- Step 1: scrape ------------------------------------------------------------------------

async function scrapeShorts(client) {
  console.log(`Scraping YouTube Shorts via ${ACTOR_ID} for ${SEARCH_QUERIES.length} querie(s)...`);

  // NOTE: sortingOrder ('views') was tried and breaks the Shorts-only search path entirely -
  // it changes YouTube's encoded search URL filter and returns zero results for every query,
  // confirmed by testing. View-count sorting is instead done client-side below (renderTopOverallTable
  // and per-query sort in buildHtmlReport), which achieves the same result without the bug.
  const run = await client.actor(ACTOR_ID).call({
    searchQueries: SEARCH_QUERIES,
    maxResults: 0, // exclude regular long-form videos
    maxResultsShorts: MAX_SHORTS_PER_SEARCH,
    downloadSubtitles: true,
    subtitlesFormat: 'plaintext',
    preferAutoGeneratedSubtitles: true,
    subtitlesLanguage: 'any',
  });

  const { items } = await client.dataset(run.defaultDatasetId).listItems();

  const byQuery = new Map(SEARCH_QUERIES.map((q) => [q, []]));
  for (const item of items) {
    if (item.error) {
      console.log(`  No Shorts found for "${item.input}" (${item.error})`);
      continue;
    }
    const query = item.input || 'unknown';
    if (!byQuery.has(query)) byQuery.set(query, []);

    const transcript = Array.isArray(item.subtitles) && item.subtitles[0]?.plaintext
      ? item.subtitles[0].plaintext.trim()
      : null;

    byQuery.get(query).push({
      title: item.title || 'Untitled',
      channelName: item.channelName || 'Unknown channel',
      viewCount: typeof item.viewCount === 'number' ? item.viewCount : null,
      url: item.url || 'N/A',
      transcript,
      description: (item.text || '').trim(),
    });
  }

  return byQuery;
}

// ---- Step 2: HTML report --------------------------------------------------------------------

function renderVideoBlock(video, rank) {
  const transcriptBody = video.transcript
    ? escapeHtml(video.transcript).replace(/\n/g, '<br>')
    : `--- [Speech Transcript Unavailable / Visual Loop Only] ---<br><br>${escapeHtml(video.description) || '<em>No description available either.</em>'}`;

  return `
    <div class="video-card">
      <div class="video-rank">#${rank}</div>
      <div class="video-title">${escapeHtml(video.title)}</div>
      <div class="video-meta">
        Channel: <strong>${escapeHtml(video.channelName)}</strong> &nbsp;|&nbsp;
        Views: <strong>${formatNumber(video.viewCount)}</strong> &nbsp;|&nbsp;
        <a href="${escapeHtml(video.url)}">${escapeHtml(video.url)}</a>
      </div>
      <div class="transcript">${transcriptBody}</div>
    </div>
  `;
}

function renderTopOverallTable(allVideos) {
  const top = [...allVideos]
    .sort((a, b) => (b.viewCount ?? 0) - (a.viewCount ?? 0))
    .slice(0, TOP_OVERALL_COUNT);

  const rows = top
    .map((v, i) => `
      <tr>
        <td>${i + 1}</td>
        <td>${escapeHtml(v.title)}</td>
        <td>${escapeHtml(v.channelName)}</td>
        <td class="num">${formatNumber(v.viewCount)}</td>
        <td><a href="${escapeHtml(v.url)}">${escapeHtml(v.url)}</a></td>
      </tr>
    `)
    .join('');

  return `
    <table class="summary-table">
      <thead>
        <tr><th>#</th><th>Title</th><th>Channel</th><th>Views</th><th>URL</th></tr>
      </thead>
      <tbody>${rows}</tbody>
    </table>
  `;
}


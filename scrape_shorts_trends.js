// ---- Step 3: PDF via WeasyPrint ---------------------------------------------------------------

function convertHtmlToPdf(htmlPath, pdfPath) {
  console.log('Converting HTML to PDF via WeasyPrint...');
  execFileSync(WEASYPRINT_BIN, [htmlPath, pdfPath], {
    env: { ...process.env, DYLD_LIBRARY_PATH: HOMEBREW_LIB_DIR },
    stdio: 'inherit',
  });
}

// ---- Orchestration ------------------------------------------------------------------------------

async function main() {
  const apifyToken = requireEnv('APIFY_API_TOKEN');
  const client = new ApifyClient({ token: apifyToken });

  const byQuery = await scrapeShorts(client);

  console.log('Building HTML report...');
  const html = buildHtmlReport(byQuery);
  fs.writeFileSync(HTML_OUTPUT_PATH, html);
  console.log(`Wrote ${HTML_OUTPUT_PATH}`);

  convertHtmlToPdf(HTML_OUTPUT_PATH, PDF_OUTPUT_PATH);
  console.log(`Done. PDF report: ${PDF_OUTPUT_PATH}`);
}

if (require.main === module) {
  main().catch((err) => {
    console.error('Failed:', err.message);
    process.exitCode = 1;
  });
}

module.exports = { buildHtmlReport, convertHtmlToPdf, HTML_OUTPUT_PATH, PDF_OUTPUT_PATH };
